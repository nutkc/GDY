package cn.nutkc.domain;

public class Record {
	private ProduceType type;
	private int value;
	private int length;
	public ProduceType getType() {
		return type;
	}
	public void setType(ProduceType type) {
		this.type = type;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	public int getLength() {
		return length;
	}
	public void setLength(int length) {
		this.length = length;
	}
	
}
